package com.sorm.po;

public class Dept {

	private Integer num;
	private String name;
	private Integer id;


	public Integer getNum(){
		return num;
	}
	public String getName(){
		return name;
	}
	public Integer getId(){
		return id;
	}
	public void setNum(Integer num){
		this.num=num;
	}
	public void setName(String name){
		this.name=name;
	}
	public void setId(Integer id){
		this.id=id;
	}
}
